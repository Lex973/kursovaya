def _add_cell_borders(tc):
    """Добавляет одинарные границы к ячейке таблицы через XML."""
    from docx.oxml import OxmlElement
    from docx.oxml.ns import qn as _qn
    tcPr = tc.get_or_add_tcPr()
    tcBorders = OxmlElement('w:tcBorders')
    for side in ('top', 'left', 'bottom', 'right'):
        border = OxmlElement(f'w:{side}')
        border.set(_qn('w:val'),   'single')
        border.set(_qn('w:sz'),    '4')
        border.set(_qn('w:space'), '0')
        border.set(_qn('w:color'), '000000')
        tcBorders.append(border)
    tcPr.append(tcBorders)


def _shade_cell(tc, fill='D5E8F0'):
    """Заливает ячейку цветом (для заголовка)."""
    from docx.oxml import OxmlElement
    from docx.oxml.ns import qn as _qn
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(_qn('w:color'), 'auto')
    shd.set(_qn('w:fill'),  fill)
    shd.set(_qn('w:val'),   'clear')
    tcPr.append(shd)


def TABLE(caption, headers, rows):
    """Добавляет заголовок таблицы, саму таблицу и пустую строку после."""
    pc = doc.add_paragraph()
    pc.style = S_NORMAL
    _set_pf(pc, first_indent=False)
    pc.paragraph_format.space_before = Pt(6)
    pc.paragraph_format.space_after  = Pt(3)
    pc.add_run(caption)

    tbl = doc.add_table(rows=1 + len(rows), cols=len(headers))

    # Применяем стиль Normal Table по id (единственный табличный стиль)
    s_tbl = next((s for s in doc.styles if s.style_id == 'a1'), None)
    if s_tbl:
        tbl.style = s_tbl

    hdr = tbl.rows[0]
    for i, h in enumerate(headers):
        cell = hdr.cells[i]
        cell.text = h
        _add_cell_borders(cell._tc)
        _shade_cell(cell._tc)
        for p in cell.paragraphs:
            for r in p.runs:
                r.bold = True
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            p.paragraph_format.space_before = Pt(2)
            p.paragraph_format.space_after  = Pt(2)

    for ri, row_data in enumerate(rows):
        row = tbl.rows[ri + 1]
        for ci, val in enumerate(row_data):
            cell = row.cells[ci]
            cell.text = str(val)
            _add_cell_borders(cell._tc)
            for p in cell.paragraphs:
                p.paragraph_format.space_before = Pt(2)
                p.paragraph_format.space_after  = Pt(2)

    P()  # пустая строка после таблицы
    return tbl


