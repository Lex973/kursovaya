        p.paragraph_format.space_before = Pt(0)
        p.paragraph_format.space_after  = Pt(0)
        p.paragraph_format.first_line_indent = Pt(0)
        if line:
            r = p.add_run(line)
            r.font.name = 'Courier New'
            r.font.size = Pt(10)


