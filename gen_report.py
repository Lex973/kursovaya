# ─── Вспомогательные функции ────────────────────────────────────────────────

def _set_pf(para, first_indent=True, ls=WD_LINE_SPACING.ONE_POINT_FIVE):
    pf = para.paragraph_format
    pf.line_spacing_rule = ls
    pf.space_before = Pt(0)
    pf.space_after  = Pt(0)
    if first_indent:
        pf.first_line_indent = Emu(450215)
    else:
        pf.first_line_indent = Pt(0)


def P(text='', bold=False, italic=False,
      align=None, size_pt=None, first_indent=True,
      space_before=None, space_after=None):
    """Абзац Normal-стиля."""
    para = doc.add_paragraph()
    para.style = S_NORMAL
    _set_pf(para, first_indent=first_indent)
    if align is not None:
        para.alignment = align
    if space_before is not None:
        para.paragraph_format.space_before = Pt(space_before)
    if space_after is not None:
        para.paragraph_format.space_after = Pt(space_after)
    if text:
        run = para.add_run(text)
        run.bold   = bold
        run.italic = italic
        if size_pt:
            run.font.size = Pt(size_pt)
    return para


